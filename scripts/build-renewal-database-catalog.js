import { createHash } from 'node:crypto';
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import {
  ADVENTURE_RULES,
  ARCHETYPES,
  BALANCE_VERSION,
  BONUS_DROP_RULES,
  COLLECTION_RULES,
  ENHANCEMENT,
  EX_DISTRIBUTION_RULES,
  GAME_RULES,
  MATERIAL_RULES,
  MINI_GAME_RULES,
  PACKS,
  RARITIES,
  REGIONS,
  REWARD_RULES,
  SOOP_RULES,
  STAGES,
  SUPPORT_ITEMS,
  SUPPORT_PACK,
  WORLD_BOSS_RULES,
} from '../src/renewal/config.js';

const root = fileURLToPath(new URL('..', import.meta.url));
const cardsPath = fileURLToPath(new URL('../data/renewal-cards.json', import.meta.url));
const outputPath = fileURLToPath(new URL('../supabase/renewal_migration_002_catalog_and_balance.sql', import.meta.url));
const COMBAT_RARITIES = ['F', 'E', 'D', 'C', 'B', 'A', 'S', 'SS', 'SSS'];
const RACES = ['저그', '테란', '프로토스'];
const ARCHETYPE_IDS = ['quick', 'heavy', 'combo', 'area', 'boss', 'amplify', 'weaken', 'sustain'];

function canonicalize(value) {
  if (Array.isArray(value)) return value.map(canonicalize);
  if (!value || typeof value !== 'object') return value;
  return Object.keys(value).sort().reduce((result, key) => {
    result[key] = canonicalize(value[key]);
    return result;
  }, {});
}

function stableJson(value) {
  return JSON.stringify(canonicalize(value));
}

function digest(value) {
  return createHash('sha256').update(stableJson(value)).digest('hex');
}

function sqlValue(value) {
  if (value === null || value === undefined) return 'null';
  if (typeof value === 'boolean') return value ? 'true' : 'false';
  return `'${String(value).replaceAll("'", "''")}'`;
}

function validateCards(cards) {
  if (!Array.isArray(cards) || cards.length !== 212) throw new Error(`Expected 212 cards, received ${cards?.length ?? 'invalid'}`);
  if (new Set(cards.map((card) => card.id)).size !== cards.length) throw new Error('Duplicate card ID');
  const expectedRarityCounts = { F: 24, E: 24, D: 24, C: 24, B: 24, A: 24, S: 24, SS: 22, SSS: 14, EX: 8 };
  Object.entries(expectedRarityCounts).forEach(([rarity, expected]) => {
    const actual = cards.filter((card) => card.rarity === rarity).length;
    if (actual !== expected) throw new Error(`${rarity} count mismatch: expected ${expected}, received ${actual}`);
  });
  cards.forEach((card) => {
    if (!/^[a-z0-9-]+$/.test(card.id)) throw new Error(`Invalid card ID: ${card.id}`);
    if (typeof card.member !== 'string' || !card.member.trim()) throw new Error(`Missing member: ${card.id}`);
    if (typeof card.file !== 'string' || /[\\/]/.test(card.file)) throw new Error(`Invalid asset filename: ${card.id}`);
    if (card.rarity === 'EX') {
      if (!card.group || card.race !== 'EX' || card.archetype !== null) throw new Error(`Invalid EX card: ${card.id}`);
    } else if (!RACES.includes(card.race) || !ARCHETYPE_IDS.includes(card.archetype) || card.group) {
      throw new Error(`Invalid combat card: ${card.id}`);
    }
  });
}

export function buildBalancePayload() {
  return {
    balanceVersion: BALANCE_VERSION,
    rarities: RARITIES,
    archetypes: ARCHETYPES,
    enhancement: ENHANCEMENT,
    materialRules: MATERIAL_RULES,
    packs: PACKS,
    supportPack: SUPPORT_PACK,
    supportItems: SUPPORT_ITEMS,
    bonusDropRules: BONUS_DROP_RULES,
    regions: REGIONS,
    stages: STAGES,
    gameRules: GAME_RULES,
    adventureRules: ADVENTURE_RULES,
    rewardRules: REWARD_RULES,
    collectionRules: COLLECTION_RULES,
    miniGameRules: MINI_GAME_RULES,
    worldBossRules: WORLD_BOSS_RULES,
    soopRules: SOOP_RULES,
    exDistributionRules: EX_DISTRIBUTION_RULES,
  };
}

export function buildCatalogMigration(cards) {
  validateCards(cards);
  const sortedCards = [...cards].sort((left, right) => left.id.localeCompare(right.id));
  const balance = buildBalancePayload();
  const configHash = digest(balance);
  const catalogHash = digest(sortedCards.map((card) => ({
    id: card.id,
    member: card.member,
    file: card.file,
    rarity: card.rarity,
    race: card.race,
    archetype: card.archetype,
    sourceRarity: card.sourceRarity ?? null,
    group: Boolean(card.group),
  })));
  const values = sortedCards.map((card) => `  (${[
    sqlValue(card.id),
    sqlValue(card.member.trim()),
    sqlValue(card.file),
    sqlValue(card.rarity),
    sqlValue(card.race),
    sqlValue(card.archetype),
    sqlValue(card.sourceRarity ?? null),
    sqlValue(Boolean(card.group)),
    sqlValue(BALANCE_VERSION),
  ].join(', ')})`).join(',\n');
  const payload = JSON.stringify(balance);

  return `-- Card Gacha Season 2: server-authoritative card catalog and balance snapshot.
-- Generated by scripts/build-renewal-database-catalog.js. Do not edit seed rows manually.
-- REVIEW ONLY. Do not run against production until migration 001 import is approved.

begin;

do $$
begin
  if to_regclass('public.gacha_s2_player_cards') is null then
    raise exception 'missing Season 2 base schema: run migration 001 first';
  end if;
end;
$$;

create table if not exists public.gacha_s2_balance_versions (
  version text primary key,
  config_hash text not null check (config_hash ~ '^[0-9a-f]{64}$'),
  catalog_hash text not null check (catalog_hash ~ '^[0-9a-f]{64}$'),
  config jsonb not null check (jsonb_typeof(config) = 'object'),
  active boolean not null default false,
  created_at timestamptz not null default now(),
  activated_at timestamptz
);

create unique index if not exists uq_gacha_s2_one_active_balance
  on public.gacha_s2_balance_versions(active) where active;

create table if not exists public.gacha_s2_card_catalog (
  card_id text primary key check (card_id ~ '^[a-z0-9-]+$'),
  member text not null check (length(trim(member)) between 1 and 40),
  asset_file text not null unique check (asset_file !~ '[\\\\/]'),
  rarity text not null check (rarity in ('F','E','D','C','B','A','S','SS','SSS','EX')),
  race text,
  archetype text,
  source_rarity text,
  is_group boolean not null default false,
  balance_version text not null references public.gacha_s2_balance_versions(version),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  check (
    (rarity = 'EX' and race = 'EX' and archetype is null and is_group)
    or (
      rarity <> 'EX'
      and race in ('저그','테란','프로토스')
      and archetype in ('quick','heavy','combo','area','boss','amplify','weaken','sustain')
      and not is_group
    )
  )
);

alter table public.gacha_s2_balance_versions enable row level security;
alter table public.gacha_s2_card_catalog enable row level security;

update public.gacha_s2_balance_versions set active = false where active and version <> ${sqlValue(BALANCE_VERSION)};

insert into public.gacha_s2_balance_versions (version, config_hash, catalog_hash, config, active, activated_at)
values (
  ${sqlValue(BALANCE_VERSION)},
  '${configHash}',
  '${catalogHash}',
  $balance$${payload}$balance$::jsonb,
  true,
  now()
)
on conflict (version) do update set
  config_hash = excluded.config_hash,
  catalog_hash = excluded.catalog_hash,
  config = excluded.config,
  active = true,
  activated_at = excluded.activated_at;

insert into public.gacha_s2_card_catalog (
  card_id, member, asset_file, rarity, race, archetype, source_rarity, is_group, balance_version
)
values
${values}
on conflict (card_id) do update set
  member = excluded.member,
  asset_file = excluded.asset_file,
  rarity = excluded.rarity,
  race = excluded.race,
  archetype = excluded.archetype,
  source_rarity = excluded.source_rarity,
  is_group = excluded.is_group,
  balance_version = excluded.balance_version,
  updated_at = now();

do $$
declare
  v_total integer;
  v_config_hash text;
  v_catalog_hash text;
begin
  select count(*) into v_total from public.gacha_s2_card_catalog;
  if v_total <> 212 then raise exception 'Season 2 catalog must contain exactly 212 cards, found %', v_total; end if;
  if (select count(*) from public.gacha_s2_card_catalog where rarity = 'EX') <> 8 then
    raise exception 'Season 2 catalog must contain exactly 8 EX cards';
  end if;
  if exists (
    select 1 from public.gacha_s2_card_catalog
    where rarity <> 'EX'
    group by rarity
    having count(distinct archetype) <> 8
  ) then
    raise exception 'every combat rarity must contain all 8 archetypes';
  end if;
  select config_hash, catalog_hash into v_config_hash, v_catalog_hash
  from public.gacha_s2_balance_versions
  where version = ${sqlValue(BALANCE_VERSION)} and active;
  if v_config_hash is distinct from '${configHash}' then raise exception 'balance config hash mismatch'; end if;
  if v_catalog_hash is distinct from '${catalogHash}' then raise exception 'catalog hash mismatch'; end if;
end;
$$;

do $$
begin
  if not exists (
    select 1 from pg_constraint
    where conname = 'gacha_s2_player_cards_catalog_fk'
      and conrelid = 'public.gacha_s2_player_cards'::regclass
  ) then
    alter table public.gacha_s2_player_cards
      add constraint gacha_s2_player_cards_catalog_fk
      foreign key (card_id) references public.gacha_s2_card_catalog(card_id);
  end if;
end;
$$;

revoke all on table public.gacha_s2_balance_versions from public, anon, authenticated;
revoke all on table public.gacha_s2_card_catalog from public, anon, authenticated;

commit;
`;
}

export function loadCards() {
  return JSON.parse(readFileSync(cardsPath, 'utf8'));
}

if (process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1]) {
  writeFileSync(outputPath, buildCatalogMigration(loadCards()), 'utf8');
  console.log(`generated ${outputPath.replace(`${root}\\`, '')}`);
}
